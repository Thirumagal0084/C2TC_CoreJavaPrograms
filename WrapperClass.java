package tnsif.wrapperclassdemo;

public class WrapperClass {

	public static void main(String[] args) {
	//Wapper class Example

		
		int x = 25;
		
		Integer y = Integer.valueOf(x);
		
		
		System.out.println(y);
		
		
		//Conversion
		
		int z = y;
		
		System.out.println(z);

	}

}
